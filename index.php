              <hr class="col-sm-12">
              <h2>Estructuras de metal</h2>
              <img src="https://paginas.seccionamarilla.com.mx/img/2685/068.jpg" style="height:300px;width:300px;" class="img-circle pull-right">Son estructuras conformadas en más de su 80% por elementos metálicos, destacan por su rapidez de fabricación y montaje además de ser estructuras muy ligeras, resistentes y duraderas e inclusive reutilizables.
              <br>
              Algunas aplicaciones de estas estructuras se dan en sistemas de marcos rígidos o dúctiles de varios niveles para edificios corporativos, plazas comerciales y hoteles por su facilidad de colocación y transporte o en estructuras de cubierta que suelen ser muy económicas y ligeras utilizadas en bodegas y naves industriales.<br><br>
              De acuerdo a la envergadura de la construcción la misma puede variar de pesada a liviana. Esta última es generalmente aplicada a viviendas de uno o pocos pisos, siendo bastante difundido el sistema Steel Framing que significa Bastidor o Cuadro.1​ La estructura respectiva está formada por perfiles conformados a partir de chapas laminadas en frío y luego galvanizadas.
