              <h2>¿Cuál es la estructura más adecuada para mi proyecto?</h2>
              <img src="https://image.slidesharecdn.com/estructuras-150115094118-conversion-gate01/95/tipos-de-estructuras-1-638.jpg?cb=1421315113"  class="img-circle pull-right">Parte fundamental de toda planeación arquitectónica y de construcción radica en elegir el tipo de estructura que formará el “esqueleto” de nuestra obra, por lo que es importante conocer las necesidades específicas de nuestro inmueble, preguntarnos ¿Para qué lo vamos a utilizar?, ¿Dónde y cómo lo vamos a construir? además de contar con un diseño y cálculo estructural adecuado que garantice su seguridad y estabilidad, a continuación hablaremos sobre los parámetros y los procesos para desarrollar una estructura.
              <br><br>
              Los primeros parámetros que debemos tomar en cuenta para elegir cualquier sistema estructural son: su estabilidad con el fin de evitar volcaduras y derrumbes, su rigidez para evitar deformaciones en presencia de fenómenos naturales o incidentes y su resistencia a las cargas que sostenga como techos, losas y elementos prefabricados.
              Entre los sistemas estructurales que podemos emplear destacan dos sistemas constructivos que cumplen con los parámetros anteriores y son los más recurrentes en las construcciones, por un lado las estructuras de acero y por el otro las estructuras de concreto, cada uno con ventajas competitivas acordes al tipo de construcción a realizar.
